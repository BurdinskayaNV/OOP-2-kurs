#include <cmath> // модуль с математическими константами и функциями
//Средняя арифметическая прогрессия - функция
float sa (float a, float b)
{
   return 0.5*(a+b);  // Возврат значения из функции
}
//Средняя геометрическая прогрессия - функция 
float sg (float a, float b)
{
   return sqrt(a*b);  // Возврат значения из функции
}
